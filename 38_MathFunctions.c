#include<stdio.h>
#include<math.h>

int main(){

    //sqrt function
    printf("%f\n", sqrt(16));

    //to round a number
    //The ceil() function rounds a number upwards to its nearest integer, and the floor() method rounds a number downwards to its nearest integer, and returns the result
    printf("%f\n", ceil(1.4));
    printf("%f\n", floor(1.4));

    //to apply power
    printf("%f\n", pow(3, 4) );

    

}